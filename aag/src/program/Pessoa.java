package program;

public class Pessoa {
	String nome;
	int idade;
	
	public Pessoa(String nome) {
		this.nome = nome;
	}

	public Pessoa(int idade) {
		this.idade = idade;
	}

	public Pessoa(int idade, String nome) {
		this.idade = idade;
		this.nome = nome;
	}
	
	public Pessoa(String nome, int idade) {
		this.idade = idade;
		this.nome = nome;
	}
	
	
	
	
	
	
}
